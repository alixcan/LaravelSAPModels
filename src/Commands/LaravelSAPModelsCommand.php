    protected function exportModels($model, $name)
    {
        $exportModel = collect($model);
        $exportModel->map(function ($item) use ($name) {
            $nameSpace = str($name)->camel()->ucfirst();


            $path = $this->getSourceFilePath('SAP' . '\\'. $nameSpace.'\\'.$item[ 'table' ]);
            $this->makeDirectory(dirname($path));

            $contents = $this->getSourceFile($nameSpace, $item[ 'table' ], $item[ 'description' ]);

            if ( !$this->files->exists($path)) {
                $this->files->put($path, $contents);
                $this->info("File : {$path} created");
            } else {
                $this->info("File : {$path} already exits");
            }

        });

        $this->warn($exportModel->count().' Model Exported');
    }


    /**
     * Return the stub file path
     *
     * @return string
     *
     */
    public function getStubPath()
    {
        return __DIR__.'/../../resources/stubs/Model.stub';
    }

    /**
     **
     * Map the stub variables present in stub to its value
     *
     * @return array
     *
     */
    public function getStubVariables($nameSpace, $model, $description)
    {
        return [
            'MODEL'       => $model,
            'NAMESPACE'   => $nameSpace,
            'DESCRIPTION' => $description,
        ];
    }

    /**
     * Get the stub path and the stub variables
     *
     * @return bool|mixed|string
     *
     */
    public function getSourceFile($nameSpace, $model, $description)
    {
        return $this->getStubContents($this->getStubPath(), $this->getStubVariables($nameSpace, $model, $description));
    }


    /**
     * Replace the stub variables(key) with the desire value
     *
     * @param $stub
     * @param  array  $stubVariables
     *
     * @return bool|mixed|string
     */
    public function getStubContents($stub, $stubVariables = [])
    {
        $contents = file_get_contents($stub);

        foreach ($stubVariables as $search => $replace) {
            $contents = str_replace('$'.$search.'$', $replace, $contents);
        }

        return $contents;

    }

    /**
     * Get the full path of generate class
     *
     * @return string
     */
    public function getSourceFilePath($model)
    {
        return base_path('App\\Models').'\\'.$model.'.php';
    }

    /**
     * Build the directory for the class if necessary.
     *
     * @param  string  $path
     *
     * @return string
     */
    protected function makeDirectory($path)
    {
        if ( !$this->files->isDirectory($path)) {
            $this->files->makeDirectory($path, 0777, true, true);
        }

        return $path;
    }
